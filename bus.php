        function filterRoutes() {
            const fromCity = document.getElementById('from').value;
            const toCity = document.getElementById('to').value;
            const selectedDate = document.getElementById('date').value;
            const busType = document.getElementById('bus-type').value;
            const passengers = parseInt(document.getElementById('passengers').value);
            const sortBy = document.getElementById('sort-by').value;

            const busRoutesContainer = document.querySelector('.bus-routes');
            busRoutesContainer.innerHTML = '<div style="text-align: center; padding: 2rem;">Searching for buses...</div>';

            setTimeout(() => {
                let filteredRoutes = busRoutes.filter(route => {
                    const matchesFromCity = fromCity === 'all' || route.from === fromCity;
                    const matchesToCity = toCity === 'all' || route.to === toCity;
                    const matchesBusType = busType === 'all' || route.bus_type === busType;
                    const hasEnoughSeats = route.available_seats >= passengers;
                    return matchesFromCity && matchesToCity && matchesBusType && hasEnoughSeats;
                });

                filteredRoutes.sort((a, b) => {
                    switch(sortBy) {
                        case 'price':
                            return a.price - b.price;
                        case 'rating':
                            return b.rating - a.rating;
                        case 'departure':
                            const timeA = new Date('2024/01/01 ' + a.departure);
                            const timeB = new Date('2024/01/01 ' + b.departure);
                            return timeA - timeB;
                        default:
                            return 0;
                    }
                });

                if (filteredRoutes.length === 0) {
                    busRoutesContainer.innerHTML = `
                        <div style="text-align: center; padding: 2rem; color: #666;">
                            No buses found for the selected criteria. Please try different options.
                        </div>
                    `;
                } else {
                    busRoutesContainer.innerHTML = filteredRoutes.map(route => createRouteCard(route)).join('');
                }
            }, 500);
        }

        const dateInput = document.getElementById('date');
        const today = new Date().toISOString().split('T')[0];
        dateInput.min = today;
        dateInput.value = today;

        const fromSelect = document.getElementById('from');
        const toSelect = document.getElementById('to');

        fromSelect.addEventListener('change', function() {
            Array.from(toSelect.options).forEach(option => {
                if (option.value !== 'all') {
                    option.disabled = option.value === this.value;
                }
            });
            filterRoutes();
        });

        toSelect.addEventListener('change', function() {
            Array.from(fromSelect.options).forEach(option => {
                if (option.value !== 'all') {
                    option.disabled = option.value === this.value;
                }
            });
            filterRoutes();
        });

        document.getElementById('passengers').addEventListener('change', function() {
            if (this.value < 1) this.value = 1;
            if (this.value > 6) this.value = 6;
            filterRoutes();
        });

        filterRoutes();
